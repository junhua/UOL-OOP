// This is your first C++ program!
// It demonstrates the basic structure that all C++ programs need.

// Include the input/output library
// This is like telling C++ we want to use its communication tools
#include <iostream>

// The main function - where every C++ program starts
// Think of this as the "recipe" that C++ will follow
int main() {
    // Print a message to the screen
    // std::cout is like the program's voice - it outputs text
    // << sends our message to cout
    // std::endl adds a new line (like pressing Enter)
    std::cout << "Hello, World!" << std::endl;
    
    // Return 0 to tell the computer our program worked correctly
    // This is like saying "Mission accomplished!"
    return 0;
}
